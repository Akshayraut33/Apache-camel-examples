public class Player {

    private String name;
    private int runs;

    public Player() {}

    public String getName() {
        return name;
    }

    public int getRuns() {
        return runs;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setRuns(int runs) {
        this.runs = runs;
    }
}

